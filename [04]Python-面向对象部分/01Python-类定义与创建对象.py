#   类    |   对象       实例
# class   |  object    instance


### 类的创建:
## 语法:
# class 类名(继承列表):
#     '''类的文档字符串'''
#     实例方法定义(类内的函数称为方法method)
#     类变量定义
#     类方法定义
#     静态方法定义
## 作用: 创建一个类,用于描述此类对象的行为和属性,类用于创建此类的一个或多个对象(实例)


### 对象的创建: 构造函数
## 表达式: 类名([创建传参列表])
## 作用: 创建这个类的实例对象,并返回此实例对象的引用关系

## 对象说明:
# 对象有自己的作用域和名字空间,可以为该实例添加实例变量(属性)
# 对象可以调用类方法和实例方法
# 对象可以访问类变量和实例变量


### 对象就是类的实例,所有的对象都是类的实例




