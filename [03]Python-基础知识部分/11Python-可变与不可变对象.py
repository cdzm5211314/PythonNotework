## Python中有6个标准的数据类型:
# 数字(Number), 字符串(String), 列表(List), 元组(Tuple), 集合(Set), 字典(Dict)

## 数值类型(Number): 细分为 整型(int), 浮点型(float), 布尔型(bool), 复数(complex )

## 不可变类型：
# 数值型(number), 字符串(str), 元组(tuple), 固定集合(frozenset), 字节串(bytes)

## 可变类型:
# 列表(list), 集合(set), 字典(dict), 字节数组(bytearray)


## 数据类型的区别:
# list列表:   可变类型      元素可重复      元素有序           读写
# tuple元组:  不可变类型    元素可重复      元素有序            只读
# set集合:    可变类型      元素不可重复    元素无序            读写
# dict字典:   可变类型      元素可重复      元素无序(自动正序)    读写


