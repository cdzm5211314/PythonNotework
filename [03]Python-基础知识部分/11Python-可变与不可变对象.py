## 不可变类型：
# int, float, complex, bool, str, tuple, frozenset(固定集合), bytes(字节串)

## 可变类型:
# list, dict, set(集合), bytearray(字节数组)


# 数据类型的区别:

# list列表:   可变类型      元素可重复      元素有序

# tuple元组:  不可变类型    元素可重复      元素有序        只读

# set集合:    可变类型      元素不可重复    元素无序

# dict字典:   可变类型      元素可重复      元素无序(自动正序)



