### 运算符:
## 算术运算符:
# 加(+), 减(-), 乘(*), 除(/), 地板除(//), 求余/取模(%), 冥运算(**)
# /  : 除法得到的是一个浮点数
# // : 去掉小数部分向下取整
# %  : 取余数
# ** : x ** y    ---> 表示: x的y次方


## 比较运算符:
# 大于(>), 大于等于(>=), 小于(<),小于等于(<=), 等于(==), 不等于(!=)
# 比较运算符的返回布尔类型的值


## 布尔运算:
# 取反(not), 与(and), 或(or)

# not True ---> Flase
# not Flase ---> True

# True and True ---> True
# True and Flase ---> Flase
# Flase and True ---> Flase
# False and Flase ---> Flase

# True or True ---> True
# True or Flase ---> True
# Flase or True ---> True
# False or Flase ---> Flase







